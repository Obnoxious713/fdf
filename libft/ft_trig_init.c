#include "libft.h"
#include "trig.h"

void			trig_tab_init(void)
{
	int			i;

	i = -1;
	while (++i < 360)
	{
		sin_table[i] = SIN(i * PI / 180.0);
		cos_table[i] = COS(i * PI / 180.0);
	}
}
