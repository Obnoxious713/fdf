#include "../includes/libft.h"

void		ft_matmult(int mat1[4][4], int mat2[4][4], int dst[4][4])
{
	int		i;
	int		j;

	i = -1;
	j = -1;
	while (++i < 4)
	{
		while (++j < 4)
		{
			dst[i][j] = mat1[i][0] * mat2[0][j] +
						mat1[i][1] * mat2[1][j] +
						mat1[i][2] * mat2[2][j] +
						mat1[i][3] * mat2[3][j];
		}
	}
}
