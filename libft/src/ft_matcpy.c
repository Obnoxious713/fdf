#include "../includes/libft.h"

void		ft_matcpy(int src[4][4], int dest[4][4])
{
	int		i;
	int		j;

	i = -1;
	j = -1;
	while (++i < 4)
	{
		while (++j < 4)
			dest[i][j] = src[i][j];
	}
}
